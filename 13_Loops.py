# Sometimes a programmer wants to execute a group of statements a certain number of times.
# This can be done using loops

# There are three type of loops
# 1.) For Loop      2.) While Loop     3.) Nested Loop
# For Loop
# The for loop allows you to go through all elements in the sequence, one at a time.


fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

#  we can also use range

for i in range(10):
    print(i)

for hello in "Hello":
    print(hello)

