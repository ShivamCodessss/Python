# Python while Loop
# As the name suggests, while loops execute statements while the condition is True.
# As soon as the condition becomes False, the interpreter comes out of the while loop.

count = 0;

while count <= 5 :
    print(count)
    count = count + 1


n = int(input("Enter a Number: "))
sum = 0;
i = 1;

while (i <= n):
    sum = sum + i
    i = i + 1

print(i)